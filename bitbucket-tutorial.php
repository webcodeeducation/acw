                <h2>Bitbucket</h2>
                <p>Bitbucket cloud is almost same as Github setup. Bitbucket provide private directory as free of cost that we can code and change itself. You create a repository in Bitbucket and clone it to your local system same as Github. If you already have a Bitbucket repository and a cloned version on your local system, then you're all set! You can now push changes you make locally to Bitbucket and pull updates to your local system.</p>
				<h2>Sign up for bitbucket?</h2>
				<p>To start using Bitbucket Cloud, you'll need an Atlassian account. If you don't have one already, we'll help you create one when you sign up for Bitbucket.</p>
				<p><code>When you pick a username, consider that Bitbucket appends this name to the URL for all repositories you create. For example, if your Username is Allcodingword and you have a repository called DEMO_REPO, the URL for that repository would be https://bitbucket.org/Allcodingword/DEMO_REPO</code></p>
				<h2>Set up Version Control System</h2>
				<p>A version control commit tool that supports Mercurial, Bazaar, Cogito (Git), Subversion, Monotone, and CVS. Meld is a merge and diff tool that allows you to compare two or three files and edit them in place, while updating automatically. Bitbucket supports two DVCS tools: Git and Mercurial. You can use both of these tools by typing commands from a terminal. Don't want to use the command line? Try SourceTree instead. SourceTree is an application with a user interface that lets you do everything you can do with Git and Mercurial without learning a lot of new commands.</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/basic-vcs-flow2.png"></p>
				<h2>Learn Git with Bitbucket Cloud</h2>
				<p>With Bitbucket Cloud, that means adding or commiting everything to a repository. Letâ€™s take an example!</p>
				<p>Initially, the repository you create in Bitbucket is going to be empty without any code in it. That's okay because you will start adding some files to it soon. This Bitbucket repository will be the central repository for your files, which means that others can access that repository if you give them permission.</p>
				<p>Steps to create your repository:</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/bitbucket-repository.png"></p>
				<p>The system displays the Create a new repository page.</p>
				<p>Enter repository name in the Name field of repository.</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/repository-name.png"></p>
				<p>Click Create repository. Bitbucket creates your repository and displays its Overview page.</p>
				<p>Let's explore the repository you have just created. You should be on the repository's Overview page:</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/repository-overview.png"></p>
				<p>When you click the Commits option on the menu bar, you will find that you have no commits because you have not created any content for your repository.</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/bitbucket-repository-clone.png"></p>
				<p>Click on Clone option in left sidebar on bitbucket and Clone your repository to your local system.</p>
				<p>Navigate to your home (~) directory.</p>
				<p><pre>$ cd ~</pre></p>
				<p>Create a directory to contain your repositories.</p>
				<p><pre>$ mkdir repos</pre></p>
				<p>From the terminal, update the directory you want to work in to your new repos directory.</p>
				<p><pre>$ cd ~/repos</pre></p>
				<p>From Bitbucket, go to your Demo repository.</p>
				<p>Click Clone.</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/bitbucket-repository-clone.png"></p>
				<p>Copy the highlighted clone command in repository on bitbucket.</p>
				<p>From your terminal window, paste the command you copied from Bitbucket and press Return.</p>
				<p>Enter the password for repository to clone in local system from bitbucket cloud.</p>
				<p>List the contents of your repos directory and you should see your Demo directory in it.</p>
				<h2>Import code from an existing project</h2>
				<p>To import code do this:</p>
				<p>Choose Repositories > Import repository from the menu bar.</p>
				<p>Select the Source of the code you want to import.</p>
				<p><img class="img-responsive center-block" src="media/bitbucket/bitbucket-import.png"></p>
				
